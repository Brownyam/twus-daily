def _build_stock_to_sub() -> dict[str, str]:
    """個股代號 → 主次產業（重複出現時取第一個，並印警告）。"""
    out: dict[str, str] = {}
    for sub, codes in SUBINDUSTRY_STOCKS.items():
        for code in codes:
            if code in out:
                # 同代號被歸到兩個次產業，保留先出現的，提醒維護者
                import logging
                logging.getLogger(__name__).warning(
                    f"tw_subindustry: {code} 同時在 {out[code]} 與 {sub}，保留 {out[code]}"
                )
                continue
            out[code] = sub
    return out


